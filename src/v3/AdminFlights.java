    private void SaveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveButtonMouseClicked
        //saving flights info
        if(FlightCode.getText().isEmpty() || FlightSource.getSelectedIndex()== -1 || FlightDestination.getSelectedIndex() == -1 || FlightSeats.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Please complete all fields!");
        }else{
            Flights_Class Fl = new Flights_Class(FlightCode.getText(), FlightSeats.getText());
            FlightList.add(Fl);
            
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/megairlinedb","root","");
                PreparedStatement Add = Con.prepareStatement("insert into Flightstb values(?,?,?,?,?)");
                Add.setString(1,FlightCode.getText());
                Add.setString(2, FlightSource.getSelectedItem().toString());
                Add.setString(3, FlightDestination.getSelectedItem().toString());
                Add.setString(4, FlightDepart.getDate().toString());
                Add.setString(5,FlightSeats.getText());

                int row = Add.executeUpdate();
                JOptionPane.showMessageDialog(this, "New Flight Added");
                Con.close();
                DisplayFlights();
                Clear();
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, e);

            }
        }
    }//GEN-LAST:event_SaveButtonMouseClicked

    private void DeleteButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteButtonMouseClicked
        if(KEY == ""){
            JOptionPane.showMessageDialog(this, "Select a flight");
        }else{
            try{
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/megairlinedb","root","");
                String Query ="Delete from flightstb where Flight_Code= '"+KEY+ "'";
                Statement Del = Con.createStatement();
                Del.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Flight has been successfully deleted!");
                DisplayFlights();
            }catch (Exception e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_DeleteButtonMouseClicked

