    private void AllFlightsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AllFlightsMouseClicked
        DefaultTableModel flights_model = (DefaultTableModel)AllFlights.getModel();
        Flights_Class Fl = new Flights_Class(FlightCode.getText(), FlightSeats.getText());
        if(FlightList.contains(Fl))
            FlightList.remove(Fl);
        
        int flights_index = AllFlights.getSelectedRow();
        KEY = flights_model.getValueAt(flights_index, 0).toString();
        FlightCode.setText(flights_model.getValueAt(flights_index, 0).toString());
        FlightSource.setSelectedItem(flights_model.getValueAt(flights_index, 1).toString());
        FlightDestination.setSelectedItem(flights_model.getValueAt(flights_index, 2));
        FlightSeats.setText(flights_model.getValueAt(flights_index, 4).toString());
        
        
        FlightList.remove(Fl);
    }//GEN-LAST:event_AllFlightsMouseClicked

